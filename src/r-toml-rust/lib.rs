    fn dfa_delta(curr_state: usize, mtlog: usize, mt: usize) -> usize {
        ((curr_state) << (mtlog)) | (mt)
    }

    pub fn lex(data: &[u8], mut on_tag: impl FnMut(usize, usize, u8)) -> Result<(), usize> {
        let mut pos: usize = 0;
        let mut prev: usize = 0;
        let mut nextpos: usize;
        let mut curr: usize = 2;
        while curr != 0 {
            if CENTER_REL[curr] > 0 {
                nextpos = pos.saturating_sub(CENTER_REL[curr] as usize);
                if CENTER_TAG[curr] >= 3 {
                    on_tag(prev, nextpos, CENTER_TAG[curr]);
                }
                prev = nextpos;
            }
            curr = CENTER_TABLE[dfa_delta(curr, MT_LOG, MT_LOOKUP[data[pos] as usize] as usize)]
                as usize;
            pos += 1;
            if pos == data.len() {
                if CENTER_REL[curr] > 0 {
                    nextpos = pos.saturating_sub(CENTER_REL[curr] as usize);
                    if CENTER_TAG[curr] >= 3 {
                        on_tag(prev, nextpos, CENTER_TAG[curr]);
                    }
                    prev = nextpos;
                }

                curr = CENTER_TABLE[dfa_delta(curr, MT_LOG, MT_LOOKUP[b'\n' as usize] as usize)]
                    as usize;
                pos += 1;

                if CENTER_REL[curr] > 0 {
                    nextpos = pos.saturating_sub(CENTER_REL[curr] as usize);
                    if CENTER_TAG[curr] >= 3 {
                        on_tag(prev, nextpos, CENTER_TAG[curr]);
                    }
                    prev = nextpos;
                }
                curr = 0;
            }
        }
        if pos <= data.len() { Err(pos) } else { Ok(()) }
    }

